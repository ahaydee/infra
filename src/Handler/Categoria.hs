postEditarCategoriaR :: CategoriaId -> Handler Html
postEditarCategoriaR cid = do
    _ <- runDB $ get404 cid
    ((result, _), _) <- runFormPost (formCategoria Nothing)
    case result of
        FormSuccess novaCategoria -> do 
            runDB $ replace cid novaCategoria
            redirect CatalogoR
        _ -> redirect CatalogoR